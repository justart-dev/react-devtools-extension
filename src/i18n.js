const getValue = (object, path) =>
  path.split('.').reduce((current, key) => (current ? current[key] : undefined), object);

export const defaultLanguage = 'en';

export const translate = (language, path, params = {}) => {
  const value = getValue(messages[language] || messages[defaultLanguage], path) ??
    getValue(messages[defaultLanguage], path) ??
    path;

  if (typeof value !== 'string') {
    return value;
  }

  return value.replace(/\{(\w+)\}/g, (_, key) => params[key] ?? '');
};

export const getLocale = (language) => (language === 'ko' ? 'ko-KR' : 'en-US');

